num = int(input("Digite um número de 1 a 13: "))

if num == 1:
    print("1 não é primo")
elif num == 2:
    print("2 é primo")
elif num == 3:
    print("3 é primo")
elif num == 5:
    print("5 é primo")
elif num == 7:
    print("7 é primo")
elif num == 11:
    print("11 é primo")
elif num == 13:
    print("13 é primo")
else:
    print(f"{num} não é primo")
